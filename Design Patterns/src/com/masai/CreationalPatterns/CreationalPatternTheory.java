package com.masai.CreationalPatterns;

public class CreationalPatternTheory {
   /* Creational design patterns are concerned with the way of creating objects.
    These design patterns are used when a decision must be made at the time of instantiation of a class
    (i.e. creating an object of a class).

    But everyone knows an object is created by using new keyword in java. For example:

    StudentRecord s1=new StudentRecord();

    Hard-Coded code is not the good programming approach. Here, we are creating the instance
    by using the new keyword. Sometimes, the nature of the object must be changed according to the
    nature of the program. In such cases, we must get the help of creational design patterns to
    provide more general and flexible approach. */
}
